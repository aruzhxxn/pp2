# math.py

import math
import random

# built-in math
print(min(1, 5, 3))
print(max(1, 5, 3))
print(abs(-7))
print(round(3.4))
print(pow(2, 3))

# math module
print(math.sqrt(9))
print(math.ceil(4.1))
print(math.floor(4.9))
print(math.pi)

# random
print(random.random())
print(random.randint(1, 10))

names = ["Ali", "Dana", "Aruzhan"]
print(random.choice(names))